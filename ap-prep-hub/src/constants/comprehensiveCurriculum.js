// Helper functions for curriculum access
export const getCurriculumData = (subject) => {
  // First try direct lookup
  if (AP_CURRICULUM_COMPREHENSIVE[subject]) {
    return AP_CURRICULUM_COMPREHENSIVE[subject];
  }
  
  // Then try normalized lookup for backwards compatibility
  const normalizedSubject = subject.toLowerCase().replace(/\s+/g, '').replace(/-/g, '');
  const subjectMap = {
    'arthistory': 'artHistory',
    'aparthistory': 'artHistory',
    'apbiology': 'biology',
    'biology': 'biology',
    'apcalculusab': 'calculusAB',
    'calculusab': 'calculusAB',
    'apcalculusbc': 'calculusBC', 
    'calculusbc': 'calculusBC',
    'apchemistry': 'chemistry',
    'chemistry': 'chemistry'
  };
  
  const mappedSubject = subjectMap[normalizedSubject] || normalizedSubject;
  return AP_CURRICULUM_COMPREHENSIVE[mappedSubject] || null;
};

// Generate contextual information for AI prompts
export const generateCurriculumContext = (subject) => {
  const curriculum = getCurriculumData(subject);
  if (!curriculum) return "";
  
  return `
OFFICIAL AP ${curriculum.name.toUpperCase()} CURRICULUM CONTEXT:

Course Description: ${curriculum.description}

Exam Format: ${curriculum.examFormat.duration}
${curriculum.examFormat.sections.map(section => 
  `- ${section.name}: ${section.questions} questions, ${section.time}, ${section.weight}`
).join('\n')}

Big Ideas:
${curriculum.bigIdeas.map(idea => `• ${idea}`).join('\n')}

Key Units (with exam weights):
${curriculum.units.map(unit => 
  `• ${unit.name} (${unit.weight}): ${unit.topics.slice(0, 4).join(', ')}${unit.topics.length > 4 ? '...' : ''}`
).join('\n')}

Essential Skills:
${curriculum.keySkills.map(skill => `• ${skill}`).join('\n')}

Study Recommendations:
${curriculum.studyTips.map(tip => `• ${tip}`).join('\n')}
`;
};

// Get all available subjects
export const getAvailableSubjects = () => {
  return Object.keys(AP_CURRICULUM_COMPREHENSIVE);
};

// Get subject units
export const getSubjectUnits = (subject) => {
  const curriculum = getCurriculumData(subject);
  return curriculum ? curriculum.units : [];
};

// Get subject topics
export const getSubjectTopics = (subject) => {
  const units = getSubjectUnits(subject);
  return units.flatMap(unit => unit.topics);
};

export default AP_CURRICULUM_COMPREHENSIVE;
